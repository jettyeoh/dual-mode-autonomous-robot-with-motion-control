/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_USART2_UART_Init();
  MX_TIM4_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  /* USER CODE BEGIN 2 */
    //initialize motor
	Motor_Init();
	Set_Speed(0, 0);
	//initialize servo
	Servo_Init();
	Set_FrontBack(0);
	Set_LeftRight(0);
	Set_UpDown(0);
	Close_Claw();
	//initialize receiver
	HAL_UART_Receive_IT(&huart2, (uint8_t *)&rxBuffer[rxIndex], 1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
		//change operating mode(autonomous to manual)
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_11) == 1 && pushbutton == 0)
		{
			HAL_GPIO_TogglePin(GPIOB, GPIO_PIN_10);
			pushbutton = 1;
			manual = !manual;
			Set_Speed(0, 0);
		}
		if(HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_11) == 0 && pushbutton == 1)
		{
			pushbutton = 0;
		}

		//get distance from sr04
		GET_Distance();

		//line following
		//need to make a source file
		if(manual == 0)
		{
			LineFollowing_Update();

			//obstacle detection
			if(distance < 10)
			{
				Set_Speed(0,0);
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_SET);
			}
			else
			{
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_1, GPIO_PIN_RESET);
			}
		}

		//manual mode
		if (messageReady) {
			ProcessReceivedData(rxBuffer);
			messageReady = 0; // Reset the flag after processing
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

